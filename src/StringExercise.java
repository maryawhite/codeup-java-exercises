public class StringExercise {

    public static void main(String[] args) {

        String lyrics = "We don't need no education\n" + "We don't need no thought control";
        System.out.println(lyrics);

        String check = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(check);

        String mainDrive = "In windows, the main drive is usually C:\\";
        System.out.println(mainDrive);

        String slashes = "I can do backslashes \\, double backslashes \\\\,\n" + "and the amazing triple backslash \\\\\\!";
        System.out.println(slashes);

    }
}
