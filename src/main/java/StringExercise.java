public class StringExercise {

    public static void main(String[] args) {
        String aString;
        aString = "We don't need no education\n" + "We don't need no thought control";
        System.out.println(aString);

        aString = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(aString);

        aString = "In windows, the main drive is usually C:\\";
        System.out.println(aString);

        aString = "I can do backslashes \\, double backslashes \\\\,\n" + "and the amazing triple backslash \\\\\\!";
        System.out.println(aString);

    }
}